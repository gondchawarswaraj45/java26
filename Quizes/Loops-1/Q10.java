class Core2web{

	public static void main(String[] args){
	
		int x = 9;

		switch(x) {
		
			case 9 :
                                System.out.println("9 as number");
                                break;
			case '9' :
                                System.out.println("9 as character");
                                break;
			case 9.0 :
                                System.out.println("9 as Double");
                                break;
			default :
                                System.out.println("9");

		}
	}
}
