class MenuIfElse {
    public static void main(String[] args) {

        int choice = 4; // user input

        if (choice == 1) {
            System.out.println("1: C");
        } 
        else if (choice == 2) {
            System.out.println("2: C++");
        } 
        else if (choice == 3) {
            System.out.println("3: DSA");
        } 
        else if (choice == 4) {
            System.out.println("4: Java");
        } 
        else {
            System.out.println("Wrong input");
        }
    }
}
