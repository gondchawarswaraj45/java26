class MenuSwitch {
    public static void main(String[] args) {

        int choice = 1;

        switch (choice) {

            case 1:
                System.out.println("1: C");
                break;

            case 2:
                System.out.println("2: C++");
                break;

            case 3:
                System.out.println("3: DSA");
                break;

            case 4:
                System.out.println("4: Java");
                break;

            default:
                System.out.println("Wrong input");
        }
    }
}
