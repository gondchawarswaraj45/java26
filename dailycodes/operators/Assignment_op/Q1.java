class MyAssign {
    public static void main(String[] args) {

        int y = 20;

        System.out.println(y -= 5); // 15
        System.out.println(y *= 2); // 30
        System.out.println(y /= 3); // 10
        System.out.println(y %= 4); // 2
        System.out.println(y);      // 2
    }
}
