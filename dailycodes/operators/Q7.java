class MyArith {
    public static void main(String[] args) {

        int x = 15;
        int y = 20;

        System.out.println(y - x); // 5
        System.out.println(y * x); // 300
        System.out.println(y / x); // 1
        System.out.println(y % x); // 5
        System.out.println(y + x); // 35
    }
}
