class StringDemo {
    public static void main(String[] args) {

        char name[] = {'k','a','n','h','a'};

        System.out.println(name);              // kanha
        System.out.println(name.toString());   // [C@hashcode

        int arr[] = {10, 20, 30};
        System.out.println(arr.toString());    // [I@hashcode
    }
}
